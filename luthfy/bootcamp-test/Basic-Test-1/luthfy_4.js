const array = ["Pisang", "Jeruk", "Apel", "Nanas", "Kiwi"];

console.log("Kalian mempunyai buah-buahan sebagai berikut: ");
console.log(array.toString());
console.log("");

console.log("Apabila daftar buah-buahan tersebut disortir berdasarkan alfabet, maka akan menjadi seperti berikut: ");
console.log(array.sort().toString());
console.log("");


console.log("Atau, kita bisa juga membaliknya menjadi seperti ini: ");
console.log(array.reverse().toString());
console.log("");
