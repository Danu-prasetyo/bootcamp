const buah = ["Pisang", "Jeruk", "Apel", "Kaos Kaki"]

console.log(`Kalian mempunyai buah-buahan sebagai berikut: \n`);
console.log(`Buah: ${buah.toString()} \n`);

console.log(`Apabila daftar buah tersebut disortir berdasarkan alfabet, maka akan menjadi seperti ini: \n`);
buah.sort()
console.log(`Buah: ${buah.toString()} \n`);

console.log(`Atau, kita bisa juga membaliknya menjadi seperti ini: \n`);
buah.reverse()
console.log(`Buah: ${buah.toString()} \n`);